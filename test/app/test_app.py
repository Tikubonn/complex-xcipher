
import pytest
import subprocess

@pytest.mark.parametrize(
  [
    "args",
    "input_",
    "expect_result",
  ],
  [
    pytest.param(
      ["-K", "0x1234567890"],
      b"",
      b"",
    ),
    pytest.param(
      ["-K", "0x1234567890", "-e"],
      b"This is plain text.",
      bytes([0x77, 0x6b, 0xad, 0x62, 0x01, 0x55, 0x29, 0x77, 0x3d, 0x83, 0xcc, 0x8a, 0x38, 0x57, 0x39, 0xb7, 0xb1, 0xc4, 0xc3, 0x37, 0xb9, 0xc6, 0x7b, 0xb3, 0xdd, 0x7e, 0xdb, 0x5c, 0x0a, 0xd7, 0x39, 0x36]),
    ),
    pytest.param(
      ["-K", "0x1234567890", "-d"],
      bytes([0x77, 0x6b, 0xad, 0x62, 0x01, 0x55, 0x29, 0x77, 0x3d, 0x83, 0xcc, 0x8a, 0x38, 0x57, 0x39, 0xb7, 0xb1, 0xc4, 0xc3, 0x37, 0xb9, 0xc6, 0x7b, 0xb3, 0xdd, 0x7e, 0xdb, 0x5c, 0x0a, 0xd7, 0x39, 0x36]),
      b"This is plain text.\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00",
    ),
    pytest.param(
      ["-K", "0x1234567890", "-d", "-p", "5"],
      bytes([0x77, 0x6b, 0xad, 0x62, 0x01, 0x55, 0x29, 0x77, 0x3d, 0x83, 0xcc, 0x8a, 0x38, 0x57, 0x39, 0xb7, 0xb1, 0xc4, 0xc3, 0x37, 0xb9, 0xc6, 0x7b, 0xb3, 0xdd, 0x7e, 0xdb, 0x5c, 0x0a, 0xd7, 0x39, 0x36]),
      b"is plain text.\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00\x00",
    ),
    pytest.param(
      ["-K", "0x1234567890", "-d", "-p", "5", "-s", "8"],
      bytes([0x77, 0x6b, 0xad, 0x62, 0x01, 0x55, 0x29, 0x77, 0x3d, 0x83, 0xcc, 0x8a, 0x38, 0x57, 0x39, 0xb7, 0xb1, 0xc4, 0xc3, 0x37, 0xb9, 0xc6, 0x7b, 0xb3, 0xdd, 0x7e, 0xdb, 0x5c, 0x0a, 0xd7, 0x39, 0x36]),
      b"is plain",
    ),
  ]
)

def test_app (
  executable:str, 
  args:list[str],
  input_:bytes,
  expect_result:bytes):
  p = subprocess.run(
    [executable] + args,
    input=input_,
    stdout=subprocess.PIPE,
    text=False,
    shell=True,
    check=True,
  )
  assert len(p.stdout) == len(expect_result)
  assert p.stdout == expect_result

@pytest.mark.parametrize(
  [
    "args",
    "input_",
  ],
  [
    pytest.param(
      ["-K", "123"],
      b""
    ),
    pytest.param(
      ["-K", "123"],
      b"123"
    ),
    pytest.param(
      ["-k", "0x123"],
      b""
    ),
    pytest.param(
      ["-k", "0x123"],
      b"123"
    ),
  ]
)
def test_app_encrypt_dectypt (
  executable:str, 
  args:list[str], 
  input_:bytes):
  p = subprocess.run(
    [executable, "-e"] + args,
    input=input_,
    stdout=subprocess.PIPE,
    text=False,
    shell=True,
    check=True,
  )
  encrypted_data = p.stdout
  p2 = subprocess.run(
    [executable, "-d"] + args,
    input=encrypted_data,
    stdout=subprocess.PIPE,
    text=False,
    shell=True,
    check=True,
  )
  assert p2.stdout[:len(input_)] == input_
